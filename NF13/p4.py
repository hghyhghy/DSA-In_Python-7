# sorting in ascending and descending order 

s=[90,55,10,20,30,40,100,50,60]

print("The list before sorting is  ")

print(s)

print("The list  after sorting ")

s.sort()

print(s)

# sorting in descending order

print("The list in the descending order is ")

s.sort(reverse=True)

print(s)
