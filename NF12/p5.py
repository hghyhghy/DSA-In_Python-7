# isdigit()

# returns if numbers are present 

a0="*****Python 1234"

print("The string is ", a0)

print(f"Is the string {a0} have any digit>>",a0.isdigit())

a2="1234"

print(f"Is the string {a2} have any digit>>",a2.isdigit())
 

